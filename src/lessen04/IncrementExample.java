package lessen04;

public class IncrementExample {
    public static void main(String[] args) {
        int x = 100;
        System.out.println("x1: " + x++);
        System.out.println("x2:"+ x);
        int y = 100;
        System.out.println("y1: " + ++y);
        System.out.println("y2: " + y);
    }
}
